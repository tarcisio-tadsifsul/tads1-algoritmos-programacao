public class testewhile {
    public static void main(String[] args) {
        int i = 1; // Inicializa i com 1
        while (i <= 10) { // Enquanto i for menor ou igual a 10
            System.out.println(i); // Imprime o valor de i
            i++; // Incrementa i em 1
        }

        int x = 10; // Inicializa x com 10
        while (x >= 1) { // Enquanto x for maior ou igual a 1
            System.out.println(x); // Imprime o valor de x
            x--; // Decrementa x em 1
        }
    }
}
