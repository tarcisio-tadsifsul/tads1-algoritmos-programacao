public class decrementoCinco {
    public static void main(String[] args) {
        int i = 50;                 // Inicializa x com 10
        while (i >= 0) {            // Enquanto x for maior ou igual a 1
            System.out.println(i);  // Imprime o valor de x
            i = i - 5;              // Decrementa x em 1
        }
    }
}
